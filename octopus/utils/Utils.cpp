#include <iostream>

#include "Utils.h"

char *charToString(char c, int n) {
  char *str = NULL;

  if (n > 0) {
    int i;

    str = new char[n + 1];    

    for (i = 0; i < n; i++)
      str[i] = c;

    str[i] = 0;
  }

  return str;
}
